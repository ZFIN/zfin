-- Reverse the duplication in pub_pages introduced by fetchPubsFromPubMed.groovy
-- (article.Pagination.text() concatenated all child text nodes — StartPage,
-- EndPage, MedlinePgn — producing strings like 'eadf5142eadf5142', or '2222'
-- when PubMed returned just <StartPage>22</StartPage><MedlinePgn>22</MedlinePgn>).
--
-- Each zdb_id below was individually validated against PubMed via efetch:
-- the proposed half exactly matches PubMed's <MedlinePgn>. The structural
-- predicate (even-length doubled string) is kept as a defensive guard, so
-- this update only writes when both conditions hold.
--
-- Out of 6,198 candidates that matched the structural predicate (post-2020,
-- non-null PMID), 6,162 were confirmed MATCH against PubMed; 24 MISMATCH and
-- 12 NOT_FOUND were excluded for follow-up handling.
--
-- The Groovy fix lands in the same release; this changeset cleans up the
-- corruption that already accumulated in production before the fix.

